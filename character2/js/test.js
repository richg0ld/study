    var division = Math.floor(arryValue.length / 100);
    var another = arryValue.length % 100;

    for(var i = 0; i <= division; i++){
        stack[i] = [];
        for(var j = 0; j < 100; j++){
            if(i === division){
                for( var k = 0;k < another; k++ ){
                    stack[i][k] = arryValue[k+(i*100)];
                }
                break;
            }
            stack[i][j] = arryValue[j+(i*100)];
        }
    }
